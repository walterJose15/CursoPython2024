"""
Practica 1
Utilizando loops For, saluda a todos los miembros de una clase, 
imprimiendo "Hola" + su nombre.

Por ejemplo: "Hola María"

alumnos_clase = ["María", "José", "Carlos", "Martina", "Isabel", "Tomás", "Daniela"]
"""
alumnos_clase = ["María", "José", "Carlos", "Martina", "Isabel", "Tomás", "Daniela"]
for item in alumnos_clase:
   print(f"Hola {item}")